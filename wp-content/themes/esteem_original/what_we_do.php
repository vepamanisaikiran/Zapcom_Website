    function isScrolledIntoView(elem) {
        var docViewTop = $(window).scrollTop();
        var docViewBottom = docViewTop + $(window).height();

        var elemTop = $(elem).offset().top;
        var elemBottom = elemTop + $(elem).height();
        return ((elemBottom <= docViewBottom) && (elemTop >= docViewTop));
    }

    function didScrolled(elem){
        var scrollPos = $(window).scrollTop();
        var elemEnd = ($(elem).offset().top)+($(elem).height()) -($(elem).height()/2);
        
        return (scrollPos >= elemEnd );
        
    }
    
     function didComeBack(elem){
        var scrollPos = $(window).scrollTop();
        var elemStart = ($(elem).offset().top);

        return (scrollPos <= elemStart );
        
    }
    
    $(window).scroll(function(){

        if (isScrolledIntoView('.container') === true) {
            $("#icon_cloud,#icon_bigdata,#icon_mobile").addClass("moved");
        }
        
        if(didScrolled('.what_we_do_section-3')  === true ){
            $("#digi_trans .content").addClass("slide");
            $("#digi_trans").addClass('clipped');
        }
        else if(didComeBack('.what_we_do_section-3') === true){
